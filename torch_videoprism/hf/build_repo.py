"""Builds a self-contained HuggingFace repo directory for one VideoPrism variant.

Usage:
    from torch_videoprism.hf.build_repo import build_hf_repo
    build_hf_repo("videoprism_public_v1_base", out_dir="/tmp/videoprism-base-pt")

The output directory is uploadable to HF as-is (e.g. via huggingface_hub.upload_folder).

What we write:
    config.json                  — VideoPrismConfig (or VideoPrismLvtConfig)
    preprocessor_config.json     — VideoPrismVideoProcessor
    model.safetensors            — converted PyTorch weights
    modeling_videoprism.py       — wrapper + inlined port code (self-contained)
    configuration_videoprism.py  — config classes
    processing_videoprism.py     — preprocessor
    README.md                    — usage example, link to the original
"""
from __future__ import annotations

import os
import shutil
import textwrap
from pathlib import Path

import torch

from torch_videoprism.hf.configuration_videoprism import VideoPrismConfig, VideoPrismLvtConfig
from torch_videoprism.hf.modeling_videoprism import VideoPrismLvtModel, VideoPrismModel
from torch_videoprism.hf.processing_videoprism import VideoPrismVideoProcessor
from torch_videoprism.model import (
    CONFIGS,
    LVT_CONFIGS,
)
from torch_videoprism.weights import load_pretrained_weights, HF_CHECKPOINTS


# Final HF repo names (Skovorp/<name>) — keyed by upstream model name.
HF_REPO_NAMES = {
    "videoprism_public_v1_base":      "videoprism-base-f16r288-pt",
    "videoprism_public_v1_large":     "videoprism-large-f8r288-pt",
    "videoprism_lvt_public_v1_base":  "videoprism-lvt-base-f16r288-pt",
    "videoprism_lvt_public_v1_large": "videoprism-lvt-large-f8r288-pt",
}


def _make_config(model_name: str) -> VideoPrismConfig | VideoPrismLvtConfig:
    """Build the HF config for `model_name`, taking values from CONFIGS."""
    cfg = CONFIGS[model_name]
    common = dict(
        patch_size=cfg["patch_size"],
        image_size=cfg["pos_emb_shape"][1] * cfg["patch_size"],
        num_frames=cfg["pos_emb_shape"][0],
        model_dim=cfg["model_dim"],
        num_spatial_layers=cfg["num_spatial_layers"],
        num_temporal_layers=cfg["num_temporal_layers"],
        num_heads=cfg["num_heads"],
        mlp_dim=cfg["mlp_dim"],
        atten_logit_cap=cfg["atten_logit_cap"],
    )
    if model_name in LVT_CONFIGS:
        return VideoPrismLvtConfig(
            **common,
            num_auxiliary_layers=cfg["num_auxiliary_layers"],
            pooler_hidden_dim=cfg.get("pooler_hidden_dim", 4 * cfg["model_dim"]),
            pooler_num_queries=cfg.get("pooler_num_queries", 1),
        )
    return VideoPrismConfig(**common)


def _build_self_contained_modeling(out_path: Path) -> None:
    """Concatenate `torch_videoprism/model.py` + the HF wrapper classes into a
    single self-contained `modeling_videoprism.py` so HF `trust_remote_code`
    loading doesn't need the `torch_videoprism` package installed.
    """
    here = Path(__file__).resolve().parent.parent
    model_src = (here / "model.py").read_text()
    wrapper_src = (here / "hf" / "modeling_videoprism.py").read_text()

    # The shipped wrapper imports `FactorizedEncoder` / `FactorizedVideoEncoder`
    # from `torch_videoprism.model`. In the inlined file those classes are
    # defined right above, so strip the import. Also drop the wrapper's
    # `from __future__ import annotations` since the inlined model.py at the
    # top of the file already has one (and __future__ imports must be the
    # first thing in a Python module).
    cleaned_wrapper = []
    skip = False
    for line in wrapper_src.splitlines():
        if line.startswith("from torch_videoprism.model import ("):
            skip = True
            continue
        if skip:
            if line.startswith(")"):
                skip = False
            continue
        if line.startswith("from torch_videoprism.model import"):
            continue
        if line.strip() == "from __future__ import annotations":
            continue
        cleaned_wrapper.append(line)
    wrapper_src = "\n".join(cleaned_wrapper)

    header = textwrap.dedent("""\
        # NOTE: This file is auto-generated by torch_videoprism/hf/build_repo.py.
        # It bundles the PyTorch port (`torch_videoprism/model.py`) and the HF
        # wrappers in one file so users can `AutoModel.from_pretrained(...,
        # trust_remote_code=True)` without `pip install torch_videoprism`.
        # Source of truth: https://github.com/Skovorp/torch_videoprism
        """)

    out_path.write_text(header + "\n" + model_src + "\n\n" + wrapper_src)


def _copy_with_substitutions(src: Path, dst: Path, replacements: dict[str, str]) -> None:
    text = src.read_text()
    for needle, repl in replacements.items():
        text = text.replace(needle, repl)
    dst.write_text(text)


