def build_hf_repo(model_name: str, out_dir: str | Path, *, weights: bool = True) -> Path:
    """Materialize a HF-loadable repo dir for one VideoPrism variant."""
    if model_name not in CONFIGS:
        raise ValueError(f"Unknown model name: {model_name!r}")

    out_dir = Path(out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    cfg = _make_config(model_name)
    is_lvt = model_name in LVT_CONFIGS

    # Build the model and (optionally) load pretrained weights.
    model_cls = VideoPrismLvtModel if is_lvt else VideoPrismModel
    model = model_cls(cfg)
    if weights:
        load_pretrained_weights(model.videoprism, model_name=model_name)

    # Wire auto_map so AutoConfig / AutoModel / AutoProcessor resolve via
    # trust_remote_code.
    auto_class = "VideoPrismLvtModel" if is_lvt else "VideoPrismModel"
    config_class = "VideoPrismLvtConfig" if is_lvt else "VideoPrismConfig"
    cfg.auto_map = {
        "AutoConfig":    f"configuration_videoprism.{config_class}",
        "AutoModel":     f"modeling_videoprism.{auto_class}",
        "AutoProcessor": "processing_videoprism.VideoPrismVideoProcessor",
    }

    # Save the model + config + preprocessor in HF format.
    model.config = cfg  # ensure auto_map is set on the saved config
    model.save_pretrained(out_dir, safe_serialization=True)

    processor = VideoPrismVideoProcessor(
        image_size=cfg.image_size,
        num_frames=cfg.num_frames,
    )
    processor.save_pretrained(out_dir)
    # The processor's auto_map gets serialized into preprocessor_config.json
    # only if we set it on the instance — patch in.
    import json
    pp_path = out_dir / "preprocessor_config.json"
    if pp_path.exists():
        d = json.loads(pp_path.read_text())
        d["auto_map"] = {"AutoProcessor": "processing_videoprism.VideoPrismVideoProcessor"}
        d["processor_class"] = "VideoPrismVideoProcessor"
        pp_path.write_text(json.dumps(d, indent=2))

    # Drop in the dynamic-loading Python files.
    here = Path(__file__).resolve().parent
    shutil.copy(here / "configuration_videoprism.py", out_dir / "configuration_videoprism.py")
    _copy_with_substitutions(
        here / "processing_videoprism.py",
        out_dir / "processing_videoprism.py",
        replacements={},  # processing has no torch_videoprism imports
    )
    _build_self_contained_modeling(out_dir / "modeling_videoprism.py")
    _write_readme(out_dir, model_name)

    return out_dir
